describe('Sumas', () => {
    // Suma de + con + tiene que ser +
    it('Suma de + con + tiene que ser +', () => {
        console.log(2 + 2);
        console.log(4);
    });
    // Suma de + y mayor que el segundo elemento es - = +
    it('Suma de + y mayor que el segundo elemento es - = +', () => {
        console.log(3 + (-2));
        console.log(1);
    });
});